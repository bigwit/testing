package itmo.cs.testing.lab2.utils;

public class ResultUnit {
	
	private double x;
	
	private double y;
	
	public ResultUnit() {
		super();
	}
	
	public double getX() {
		return x;
	}
	
	public void setX(double x) {
		this.x = x;
	}
	
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	
	
}
